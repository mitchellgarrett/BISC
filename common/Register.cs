namespace FTG.Studios.BISC
{

	/// <summary>
	/// Byte-encoded enum of each BISC register.
	/// </summary>
	public enum Register : byte
	{
		PC = 0x00, SP = 0x01, GP = 0x02, FP = 0x03, RA = 0x04, RV = 0x05,
		TI = 0x06, TA = 0x07,
		R0 = 0x08, R1 = 0x09, R2 = 0x0A, R3 = 0x0B, R4 = 0x0C, R5 = 0x0D, R6 = 0x0E, R7 = 0x0F,
		F0 = 0x10, F1 = 0x11, F2 = 0x12, F3 = 0x13, F4 = 0x14, F5 = 0x15, F6 = 0x16, F7 = 0x17,
	}
}
