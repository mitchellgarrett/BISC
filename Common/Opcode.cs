namespace FTG.Studios.BISC {

    /// <summary>
    /// Byte-encoded enum of each BISC opcode.
    /// </summary>
    public enum Opcode : byte {
        NOP = 0x00, HLT = 0x01, SYS = 0x02, CALL = 0x03, RET = 0x4,
        LLI = 0x05, LUI = 0x06, MOV = 0x07,
		LD = 0x08, LH = 0x09, LB = 0x0A, ST = 0x0B, SH = 0x0C, SB = 0x0D,
		ADD = 0x0E, SUB = 0x0F, MUL = 0x10, DIV = 0x11, MOD = 0x12, 
		NEG = 0x13, NOT = 0x14, INV = 0x15, 
		AND = 0x16, OR = 0x17, XOR = 0x18, BSL = 0x19, BSR = 0x1A,
        JMP = 0x1B, JEZ = 0x1C, JNZ = 0x1D, JEQ = 0x1E, JNE = 0x1F, JGT = 0x20, JLT = 0x21, JGE = 0x22, JLE = 0x23
    };
	
	public enum PseudoOpcode : byte {
		LI, LA, ADDI, SUBI, MULI, DIVI, MODI, INC, DEC, PUSH, POP
	}
}
